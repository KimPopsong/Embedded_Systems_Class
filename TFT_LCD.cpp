void Write(int alpha[])
{
	int fbfd;
	int ret;
	int repeatCounti, repeatCountj, count = 0;
	unsigned short white = 65535;

	struct fb_var_screeninfo fbvar;
	int offset;//frame buffer driver file point를 얻는다.

	fbfd = open(FBDEVFILE, O_RDWR);

	if (fbfd < 0)
	{
		perror("fbdevopen");
		exit(1);
	}
	// 커널로부터 LCD에 관한 정보를 fbvar에 저장한다. 

	ret = ioctl(fbfd, FBIOGET_VSCREENINFO, &fbvar);

	if (ret < 0)
	{
		perror("fbdevioctl");
		exit(1);
	}

	if (fbvar.bits_per_pixel != 16)
	{ //bpp가 16bit인지 check
		perror(" bpp is not 16\n ");
		exit(1);
	}

	for (count = 0, repeatCounti = 0; count < 576; count++, repeatCounti++) //글자 하나 출력
	{
		if (alpha[count] == 0)
		{
			continue;
		}

		else
		{
			if (repeatCounti >= 24)
				repeatCounti %= 24;

			repeatCountj = (count / 24);

			offset = (repeatCountj + writeline) * fbvar.xres * (16 / 8) + (repeatCounti + writealpha) * (16 / 8);

			if (lseek(fbfd, offset, SEEK_SET) < 0) {}

			write(fbfd, &white, 2); /*write 2bytes (16bit)*/
		}
	}

	close(fbfd);
	return;
}

