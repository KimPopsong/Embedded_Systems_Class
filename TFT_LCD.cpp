void Add(char input)
{
	int repeatCount;
	time_t duplecurrentTime = time(NULL);

	if (input == 'P') //PRS 예외처리(Q가 없기 때문)
	{
		if ((duplecurrentTime - dupleGlobalTime >= 3) || dupleFlag == 0)
		{
			dupleFlag = 1;
			rearAlpha = 'P';
		}

		else if (rearAlpha != 'P' && rearAlpha != 'R' && rearAlpha != 'S')
		{
			dupleFlag = 0;
			rearAlpha = input;
		}

		else if (rearAlpha == 'P')
		{
			Delete();
			dupleFlag = 1;
			rearAlpha = 'R';
			input = 'R';
		}

		else if (rearAlpha == 'R')
		{
			Delete();
			dupleFlag = 1;
			rearAlpha = 'S';
			input = 'S';
		}

		else if (rearAlpha == 'S')
		{
			Delete();
			dupleFlag = 1;
			rearAlpha = 'P';
			input = 'P';
		}

		else
		{
			Delete();
			dupleFlag = 1;
			rearAlpha = 'P';
		}

		dupleGlobalTime = time(NULL);
	}

	else if (input == '.') //.QZ 예외처리
	{
		if ((duplecurrentTime - dupleGlobalTime >= 3) || dupleFlag == 0)
		{
			dupleFlag = 1;
			rearAlpha = '.';
		}

		else if (rearAlpha != '.' && rearAlpha != 'Q' && rearAlpha != 'Z')
		{
			dupleFlag = 0;
			rearAlpha = input;
		}

		else if (rearAlpha == '.')
		{
			Delete();
			dupleFlag = 1;
			rearAlpha = 'Q';
			input = 'Q';
		}

		else if (rearAlpha == 'Q')
		{
			Delete();
			dupleFlag = 1;
			rearAlpha = 'Z';
			input = 'Z';
		}

		else if (rearAlpha == 'Z')
		{
			Delete();
			dupleFlag = 1;
			rearAlpha = '.';
			input = '.';
		}

		else
		{
			Delete();
			dupleFlag = 1;
			rearAlpha = '.';
		}

		dupleGlobalTime = time(NULL);
	}

	else if (dupleFlag == 0)
	{
		dupleGlobalTime = time(NULL);
		dupleFlag = 1;
		rearAlpha = input;
	}

	else if (dupleFlag == 1 && !(input <= rearAlpha && rearAlpha <= input + 2))
	{
		dupleGlobalTime = time(NULL);
		dupleFlag = 1;
		rearAlpha = input;
	}

	else //dupleFlag == 1 && input <= rearAlpha && rearAlpha <= input + 2
	{
		if (duplecurrentTime - dupleGlobalTime >= 3) //input and over 3secs
		{
			dupleFlag = 0;
		}

		else //PRS DOTQZ예외처리 필요.
		{
			Delete();
			dupleFlag = 1;
			dupleGlobalTime = time(NULL);;

			rearAlpha++;

			if (rearAlpha > input + 2)
				rearAlpha -= 3;

			input = rearAlpha;
		}
	}
	
	for (repeatCount = cursorMax; repeatCount >= cursorNow; repeatCount--)
	{
		str[repeatCount + 1] = str[repeatCount];
	}

	if (cursorMax >= 70) //화면에 70개의 글자를 띄웠다면
	{
		printf("Too many Alphabets!!\n");
		return;
	}

	else
	{
		str[cursorNow] = input;
		cursorMax++;
		cursorNow++;
	}

	WriteString();

	return;
}

void Delete()
{
	int repeatCount;
	dupleFlag = 0;
	
	if(cursorMax <= 0 || cursorNow <= 0)
	{
		printf("You Can't Delete Anymore!!\n");
		return;
	}

	else if (cursorNow == cursorMax) //cursor is rightmost
	{
		cursorNow--;
		cursorMax--;
	}

	else
	{
		for (repeatCount = cursorNow - 1; repeatCount < cursorMax - 1; repeatCount++)
		{
			str[repeatCount] = str[repeatCount + 1];
		}

		cursorNow--;
		cursorMax--;
	}

	WriteString();

	return;
}

void DrawCursor()
{
	static int cursorColorFlag = 0;
	int fbfd;
	int ret;
	int repeatCounti, repeatCountj, count = 0;
	unsigned short cursorColor = 65535;

	struct fb_var_screeninfo fbvar;
	int offset;

	fbfd = open(FBDEVFILE, O_RDWR);

	if (fbfd < 0)
	{
		perror("fbdevopen");
		exit(1);
	}

	ret = ioctl(fbfd, FBIOGET_VSCREENINFO, &fbvar);

	if (ret < 0)
	{
		perror("fbdevioctl");
		exit(1);
	}

	if (fbvar.bits_per_pixel != 16)
	{
		perror(" bpp is not 16\n ");
		exit(1);
	}

	if (cursorColorFlag == 0)
	{
		cursorColor = 65535;
		cursorColorFlag = 1;
	}

	else
	{
		cursorColor = 0;
		cursorColorFlag = 0;
	}

	for (count = 0, repeatCounti = 0; count < 576; count++, repeatCounti++) //글자 하나 출력
	{
		if (cursor[count] == 0)
		{
			continue;
		}

		else
		{
			if (repeatCounti >= 24)
				repeatCounti %= 24;

			repeatCountj = (count / 24);
			
			if(cursorNow < 71)
				offset = (repeatCountj + ((cursorNow / 10) * 32)) * fbvar.xres * (16 / 8) + (repeatCounti + (cursorNow % 10) * 28) * (16 / 8);

			else
				offset = (repeatCountj + ((cursorNow / 10 + 1) * 32)) * fbvar.xres * (16 / 8) + (repeatCounti + (cursorNow % 10) * 28) * (16 / 8);

			if (lseek(fbfd, offset, SEEK_SET) < 0) {}

			write(fbfd, &cursorColor, 2); /*write 2bytes (16bit)*/
		}
	}

	close(fbfd);
}

void MoveCursor(int caseNumber)
{
	dupleFlag = 0;

	if (caseNumber == 0) //Move Left
	{
		WriteString();

		if (cursorNow == 0)
			printf("You Can't Go Left Anymore!!\n");

		else
			cursorNow--;
	}

	else //Move Right
	{
		WriteString();

		if (cursorNow >= cursorMax)
			printf("You Can't Go Right Anymore!!\n");

		else
			cursorNow++;
	}

	return;
}

void ControlCursor()
{
	time_t cursorCurrentTime = time(NULL);

	if ((cursorCurrentTime - cursorGlobalTime) >= 1)
	{
		DrawCursor();
		cursorGlobalTime = time(NULL);
	}

	return;
}

